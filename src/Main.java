public class Main {
    public static void main(String[] args) {
        double x = 3;
        double y = 9;
        System.out.println(x + " + " + y + " = " + (x+y));
        System.out.println(x + " - " + y + " = " + (x-y));
        System.out.println(x + " * " + y + " = " + (x*y));
        System.out.println(x + " / " + y + " = " + (x/y));
        System.out.println(x + " % " + y + " = " + (x%y));
    }
}